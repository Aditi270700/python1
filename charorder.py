# char and order in python
# python char() function

# this takes in a integer i and converts it to a character c,
# so it returns a character string

# convert integer 65 to ASCII character ('A')
y=chr(65)
print(type(y),y)

s=chr(66)
print(type(s),s)

t=chr(67)
print(type(t),t)

# ord function
# this takes a single unicode character(string of length 1)
# and returns an integer, so the format is:

# convert ASCII Unicode Character 'A' to 65
y=ord('A')
print(type(y),y)

s=ord('B')
print(type(s),s)

t=ord('D')
print(type(t),t)