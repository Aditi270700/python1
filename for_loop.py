# for loop for increment range

# range(5)
#start=0
# condition<5
#increment by default 1
# 0 1 2 3 4

# range(1,6)
# start=1
# condition<6
# increment by default 1
# 1 2 3 4 5

# range(1,6,2)
# start=1
# condition<6
# increment by default 2
# 1 3 5

for n in range(5):
    print(n)
print()
for n in range(1,6):
    print(n)
print()
for n in range(1,8,3):
    print(n)
print()
for a in range(1,11):
    print(a*5)
print()
for a in range(1,11):
    print("2 *",a,"=",2*a)